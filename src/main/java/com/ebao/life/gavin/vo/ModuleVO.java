package com.ebao.life.gavin.vo;

public class ModuleVO {
	private String moduleId;
	private String moduleName;
	private String moduleDesc;
	private String moduleType;
	private String parentModule;
	private String moduleOrder;
	private String needRight;
	private String insertTime;
	
	public String getModuleId() {
		return moduleId;
	}
	public void setModuleId(String moduleId) {
		this.moduleId = moduleId;
	}
	public String getModuleName() {
		return moduleName;
	}
	public void setModuleName(String moduleName) {
		this.moduleName = moduleName;
	}
	public String getModuleDesc() {
		return moduleDesc;
	}
	public void setModuleDesc(String moduleDesc) {
		this.moduleDesc = moduleDesc;
	}
	public String getModuleType() {
		return moduleType;
	}
	public void setModuleType(String moduleType) {
		this.moduleType = moduleType;
	}
	public String getParentModule() {
		return parentModule;
	}
	public void setParentModule(String parentModule) {
		this.parentModule = parentModule;
	}
	public String getModuleOrder() {
		return moduleOrder;
	}
	public void setModuleOrder(String moduleOrder) {
		this.moduleOrder = moduleOrder;
	}
	public String getNeedRight() {
		return needRight;
	}
	public void setNeedRight(String needRight) {
		this.needRight = needRight;
	}
	public String getInsertTime() {
		return insertTime;
	}
	public void setInsertTime(String insertTime) {
		this.insertTime = insertTime;
	}

}
